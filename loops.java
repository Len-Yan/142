public class loops {
  
  // method of for loop
  public void forloop(){
    System.out.println("Using a for loop:");  
    // start of fence "1"
    System.out.print("1");
    // for loop
    for (int i = 18; i <= (205); i+=17) {
      System.out.print(", "+ i);}
    // end of fence "."
    System.out.println(".");}
  
  
// method of while loop
  public void whileloop(){  
    //init  x
    int x = 17;
    System.out.println("Using a while loop:");  
    //start of fence "1"
    System.out.print("1");
    // while loop
    while (x <= 205){
      while ((x-1) % 17 == 0) {
        System.out.print(", " + x);
        x+=1;
      } x+=1;}
    // end of fence "."
    System.out.println(".");}

  //method of dowhile loop
  public void dowhileloop(){
    int x = 17;
    System.out.println("Using a do-while loop:");  
    //start of fence "1"
    System.out.print("1");
    // do loop  
    do {System.out.print(", " + (x+1));
      x+=17;
    }while(x <= 205);
    // end of fence "."
    System.out.println(".");}

  //app method
  public static void main(String[] args) {
   //call for loop
    loops loops1 = new loops();
    loops1.forloop();
    //call while loop
    loops loops2 = new loops();
    loops2.whileloop();
    //call do while loop
    loops loops3 = new loops();
    loops3.dowhileloop();
  }}